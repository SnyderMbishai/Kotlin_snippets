// Dealing with integers
fun addition(a: Int, b: Int): Int{
    return a + b
}

// You can simply use one liners for simple ones
fun multiplication(a: Int, b: Int) = a * b
fun subtraction(a: Int, b: Int) = a - b

// run
fun main(){
    println(addition(4, 5))
    println(multiplication(10,10))
    println(subtraction(10,10))
}
